.MODEL SMALL
.STACK 100H
.DATA
 
 MSG1 DB 'ENTER A HEX DIGIT: $'
 MSG2 DB 'IN DECIMAL IT IS $'

 
.CODE
MAIN PROC
    
    MOV AX,@DATA
    MOV DS,AX
    
    MOV AH,9;SHOWING STRING FOR INPUT
    LEA DX,MSG1
    INT 21H
    
    MOV AH,1;TAKING HEX INPUT
    INT 21H
    MOV BL,AL
    
    MOV AH,2;FOR NEW LINE
    MOV DL,0AH
    INT 21H
    MOV DL,0DH
    INT 21H
    
    MOV AH,9;SHOWING STRING FOR RESULT
    LEA DX,MSG1
    INT 21H
    
    MOV AH,2;FROM A TO F 1 IS ALWAYS CONSTANT
    MOV DL,031H
    INT 21H
    
    SUB BL,17
    MOV AH,2
    MOV DL,BL
    INT 21H
    
    MOV AH,4CH
    INT 21H
    
    MAIN ENDP
END MAIN